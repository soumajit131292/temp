package com.BridgeLabz.Programming.oops.ricewheatpulse.model;

public class DataManagement {

	private String name;
	private int weight;
	private int price;

	// Getter Methods

	public String getName() {
		return name;
	}

	public int getWeight() {
		return weight;
	}

	public int getPrice() {
		return price;
	}

	// Setter Methods

	public void setName(String name) {
		this.name = name;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public void setPrice(int price) {
		this.price = price;
	}
	
}
